class Employee {
    constructor(nik, firstName, lastName, phone, birthDate, gender, salary, email) {
        this.nik = nik;
        this.firstName = firstName;
        this.lastName = lastName;
        this.phone = phone;
        this.birthDate = birthDate;
        this.salary = salary;
        this.email = email;
        this.gender = gender;
    }
}